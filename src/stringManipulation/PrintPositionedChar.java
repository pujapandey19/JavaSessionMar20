package stringManipulation;

public class PrintPositionedChar {

	public static void main(String[] args) {
		
		String st = "selenium";
		int strLength = st.length();
		
		System.out.println("First character of the given string is : "+ " "+ st.charAt(0));
		System.out.println("Last character of the given string is  : " + " "+(st.charAt(strLength-1)));
		

	}

}
