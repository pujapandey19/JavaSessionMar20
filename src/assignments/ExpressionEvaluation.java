package assignments;

public class ExpressionEvaluation {

	public static void main(String[] args) {

		// a. -5 + 8 * 6
		int a = -5 + 8 * 6;
		System.out.println("Output of expression a : " + " " + a);

		// b. (55+9) % 9
		int b = (55 + 9) % 9;
		System.out.println("Output of expression b : " + " " + b);

		// c. 20 + -3*5 / 8 = 19
		int c = 20 + -3 * 5 / 8;
		System.out.println("Output of expression c : " + " " + c);

		// d. 5 + 15 / 3 * 2 - 8 % 3
		int d = 5 + 15 / 3 * 2 - 8 % 3;
		System.out.println("Output of expression d : " + " " + d);

	}

}
